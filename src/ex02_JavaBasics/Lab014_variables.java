package ex02_JavaBasics;

public class Lab014_variables
{
    public static void main(String[] args)
    {
        byte b = 10;
        short s = 100;
        int i = 1000;
        long l = 10000L;
        float f = 10.0f;
        double d = 10.0;
        char c = 'a';
        boolean bool = true;
        System.out.println(b + " " + s + " " + i + " " + l + " " + f + " " + d + " " + c + " " + bool);
    }
}
