package ex09_switch;

public class Lab096_Interview {
    public static void main(String[] args) {
        char code = 'A';
        switch (code) {
            default:
                System.out.println("Hellooooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}