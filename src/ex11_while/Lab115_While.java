package ex11_while;

public class Lab115_While {
    public static void main(String[] args) {
        int i = 0;
        while (i <= 50) {
            System.out.println(i);
            i++;
        }
        int j = 0;
        while (j <= 50) {
            System.out.println(j);
            j++;
        }
    }
}