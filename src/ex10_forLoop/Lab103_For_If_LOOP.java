package ex10_forLoop;

public class Lab103_For_If_LOOP {
    public static void main(String[] args) {
        for(int raj=0; raj<=18; raj++)
            if (raj>15)
            {
                System.out.println("PAPA bought a iPhone");
            }
        else{
                System.out.println("No iPhone");
            }
    }
}
